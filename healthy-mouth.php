<p>Thoroughly cleaning our mouth should not be optional; it's an activity a majority of us should commit to if we want our bodies to function properly. A good sign of a healthy body are healthy teeth and having healthy teeth demonstrates that we are selective with what we eat. Notice that when we glance at someone, despite how beautiful they may look, if they have yellow teeth and their gums are black, we immediately are turned off in some way.</p>
<p>Colgate brand has always been an advocate of the "Healthy Mouth, Healthy Body" campaign. They've published reports on how serious gum diseases have been linked to life-threatening medical illnesses, such as diabetes and even cancer. Their advocacy to keep the public informed on the hazards of not brushing our teeth are often prioritized over their product advertisements themselves.</p>
<p>What is the most distinct association between these the mouth and the rest of the body? For one, the mouth serves as the first stage where food gets processed and sent to the esophagus. Keep in mind that the body reacts sensitively to unknown contaminants and substances that enter it. If you happen to see any swelling on your gums and some new, unsightly stains on your teeth, it's imperative you consult both your dentist and your resident doctor immediately as this could be an indicator of a gum disease or tartar buildup: both of which are health-threatening dental conditions like periodontitis.</p>
<p>Brushing your teeth sounds like a good idea, and it is indeed, but to ensure maximum protection for your mouth, gums and teeth, you should also floss and  monitor the food you consume. If you know that you are allergic to the food served on a platter, you likely won't eat it. If you also know that the food you're about to eat is high in sugar or more than your bodily norm, eat it in moderation.
Recent studies have confirmed the link of periodontitis to cardiovascular diseases such as heart diseases and strokes, as well as other systematic diseases such as diabetes, cancer and hepatitis. It is a good plan to live a healthy lifestyle while you're young, or pay the toll of unhealthy living when you've reached a ripe age, an age where you're at the point when even the strongest medications and surgeries fail to fix the ailment.
The health of the gums is a very important factor to consider. Bleeding gums are most likely to be the catalyst for more serious health issues like periodontitis and cancer because of the instant contamination of the bloodstream when bacteria makes its way into the blood, flowing from the gums.</p>
<p>Dental plaque may look harmless, but it's just the same as having periodontitis, only patients with severe cases of hardened plaque, or "tartar" are also prone to various gum diseases such as the before mentioned periodontitis.
The common idea that a sudden attack of periodontitis sets in when one has diabetes is also very true. Patients who have diabetes have twice the risk of acquiring periodontitis if they do not take care of their dental health and the rate of diabetes-induced periodontitis has sped up damage levels. Within a few months, the teeth may start falling off, due to the uncontrolled glucose levels of the body, leading to poor blood circulation to the gums.</p>
