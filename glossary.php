     <img src="images/blog-3.jpg" class="blog-fet" />
    <h3>Abscess: </h3><p>a dental infection/symptom that is indicated by gum inflammation and a large accumulation of pus surrounding the tooth.</p>
<h3>Amalgam: </h3><p>a combination of silver with mercury that forms a material used for cavity filling.</p>
<h3>Bicuspid: </h3><p> also referred to as a premolar, a bicuspid is a tooth with two points, located between the incisors and the molars, mainly used to tear and grind food.</p>
<h3>Bonding:</h3><p>also know as "adhesive dentistry," it is the process of bonding veneers or similar material to the surface of the tooth with composite adhesive material.</p>
<h3>Bridge: </h3><p>a fixed partial denture that is used to replace a missing tooth or section of teeth by attaching it to adjacent teeth. 
Bruxism: involuntary grinding and clenching of teeth, which ultimately leads to chipped/ and/or dislocated teeth, sometimes occurring during sleep.</p>
<h3>Calculus:</h3> <p>or most commonly known as "tartar,: it is the hardened deposit of plaque that causes both tooth and gum diseases when not properly treated.</p>
<h3>Caries:</h3><p> also known as "tooth decay," it is the gradual, continuous destruction of the tooth.</p>
<h3>Cavity:</h3><p>is a depressed and dying area of the tooth sourced from tooth decay.</p>
<h3>Composites:</h3><p> are synthetic resins used as restorative materials, such as dental fillings. Dental composites are made up of nontoxic material such as silicon dioxide and glass particles, which are the most preferred filling material used by dentists today because of their non-toxicity and their chemical properties that imitates tooth color.</p>
<h3>Crown ("cap"):</h3><p>dental restoration procedure that "caps" or covers a tooth or false tooth with the use of dental cement. A crown is used to replace a missing portion of a tooth.</p>
<h3>Cuspid - or the canine</h3><p> is a tooth with a singular, protruding cusp, primarily used for breaking down food.</p>
<h3>Dentin - </h3><p>is the calcareous material surrounding the pulp of the tooth and basically makes up the majority of the tooth.</p>
<h3>Dry socket or alveolitis:</h3><p> is the inflammation of the socket of the tooth, which primarily occurs when blood clots fail to form after an extraction of a molar or a wisdom tooth. Acute pain is often felt for a few days but can be deterred by consuming pain killers. Note that antibiotics have no effect in reducing the pain as a dry socket is not an infection.</p>
<h3>Enamel:</h3><p> is the hard white coating, consisting of calcified tissue that surrounds the crown of the tooth.</p>
<h3>Endodontist:</h3><p> a dentist who specializes in treating nerve infections and diseases with Endodontists commonly performing root canal surgery, known as endodontics.</p>
<h3>Fluoride:</h3><p>a substance found in toothpastes that is effective in preventing tooth decay. Rich amounts of fluoride are widely found in common drinking water.</p>
<h3>Gingivitis:</h3><p> swelling and soreness of the gums or gingival tissue that, without proper treatment, can cause periodontal disease. A progressed state of gingivitis can be accompanied by bleeding gums.</p>
<h3>Gum disease or periodontitis:</h3><p> in dental terms, is a set of illnesses or infections that inflame and subsequently destroy the gum tissue if left untreated.</p>
<h3>Halitosis:</h3><p>commonly called "bad breath," is an unpleasant odor spewing from the mouth that is often caused by a variety of factors, both medical and dental that can include: gingivitis, tooth decay or periodontitis.</p>
<h3>Impacted tooth:</h3><p> a condition/disorder in which a tooth cannot protrude normally because another tooth is blocking it's route to do so.</p>
<h3>Incisor:</h3><p> a chisel-shaped tooth located at the front of the mouth. Incisors are used primarily in sawing down or gnawing food into several, more manageable pieces.</p>
<h3>Malocclusion or a bad bite relationship:</h3><p> is a dental condition that is exhibited by the misalignment or protrusion of a tooth or set of teeth.</p>
<h3>Molars:</h3><p> are the rearmost teeth in the human body, primarily used to grind down food so it can be more easily digested by the stomach.</p><h3>
Mucin:</h3><p>is a nitrogenous substance found in the saliva that, when mixed with sugars, form bacterial plaque.</p>
<h3>Orthodontist:</h3><p>a dentist specializing in the treatment of malocclusions and other irregularities of the teeth, also known as the practice of orthodontics.</p>
<h3>Pedodontist:</h3><p> a dentist who specializes in the treatment of children's teeth, also known as pedodontics.</p>
<h3>Periodontist:</h3><p>a dentist specializing in the treatment of gum diseases, periodontal diseases and sometimes TMJ symptoms.</p>
<h3>Periodontal Disease:</h3><p> a disease which attacks the gum and commonly begins with gingivitis and progresses to more aggressive forms of diseases that affect the gum. Periodontal diseases can come from either negligent oral hygiene or from medical conditions such as diabetes or cancer.</p>
<h3>Plaque:</h3><p>is the sticky thin film consisting of bacteria and other microorganisms that fixate onto the tooth surface. When left untreated, plaque leads to tartar, dental cavities and periodontitis.</p>
<h3>Prosthodontist:</h3><p> a dentist who specializes in creating prosthetic teeth and other dental apparatus for patients who have missing teeth, also known as prosthodontics.</p>
<h3>Pulp:</h3><p>is the central part of the tooth that consist of living tissue, blood vessels, nerves and tooth cells called odontoblasts.</p>
<h3>Pulpitis ¨C</h3><p> is an infection of a nerve inside of the tooth in which the dental pulp becomes inflamed because of trauma from an accident, often signaled by acute toothaches.</p>
<h3>Radiographs:</h3><p> are X-ray pictures of the teeth, mouth and the jaw commonly used when locating an impacted tooth or finding the extent of the root of the teeth before applying dental apparatus such as dentures, braces, etc.</p>
<h3>Root Canal (or Root Canal Therapy):</h3><p> is the process of removing the pulp of a tooth and cleaning, shaping and subsequently filling the inside of the pulp chamber. A root canal therapy is used to salvage the damaged tooth, later to be covered by a jacket or a crown.</p>
<h3>Wisdom teeth ¨C or the "third molar teeth"</h3><p> are the final set of teeth that usually protrude from ages 15-25. The eruption of these teeth can sometimes pose serious problems, especially if there is another tooth blocking the wisdom teeth's opening. The average adult has four wisdom teeth: a pair in both the upper and lower jaws. Removal of an impacted wisdom tooth often results in a dry socket.</p>


